package aoc

import (
	"strings"
	"testing"
)

func Test_twenty(t *testing.T) {
	text := "###############\n#...#...#.....#\n#.#.#.#.#.###.#\n#S#...#.#.#...#\n#######.#.#.###\n#######.#.#...#\n#######.#.###.#\n###..E#...#...#\n###.#######.###\n#...###...#...#\n#.#####.#.###.#\n#.#...#.#.#...#\n#.#.#.#.#.#.###\n#...#...#...###\n###############\n"
	result := twenty(strings.Split(text, "\n"), 40)

	if result != 2 {
		t.Fail()
	}
}
